                                <p class="bold">Consumer access to cannabis related products is increasing as states ease restrictions on medical and recreational cannabis.</p>
                                <p>With increased availability, consumers are demanding testing and quality control governess. While global standards for cannabis testing have not yet been adopted in many states, cannabis products must undergo testing to ensure they are safe for consumption.</p>
                                <p class="bold">While testing requirements vary from state to state, common analyses include:</p>
                                <p>Potency testing to confirm the amount of cannabidiol, tetrahydrocannabinol, cannabinol, cannabichromene and/or cannabigerol. Analyzing the terpene content of different strains is crucial for developing targeted treatment plans. Also, the quantification of linalool, myrcene, limonene, terpinolene, α-pinene, β-pinene, humulene, caryophyllene and/or caryophyllene oxide are common place.</p>
                                <p>Organic solvents are used to extract essential oils out of cannabis to create concentrated extracts that can be used to create downstream products. Post extraction the solvents are removed by evaporation to create oils, waxes or shatters. To ensure consumer safety, any residual solvent must be quantified to ensure the finished goods are free of potentially harmful chemicals.</p>
                            </div>
                            <div class="col-md-6">
                                <p class="bold">As with many consumer food products, heavy metals, pesticides and microbial contamination is a concern.</p>
                                <p>Heavy metals are quantified routinely by acid digestion and ICP-MS while microbial bioburden can be performed by plating methods or through the more sensitive PCR targeted assays.</p>
                                <p>Pesticide residue analysis is performed through QuEChERS (“Quick, Easy, Cheap, Effective, Rugged and Safe”) extraction followed by LC-MS/MS to quantify the amount of pesticides present in the sample of interest.</p>
                                <p>In all cases, cannabis samples, including flowers and edibles must be ground prior to extraction. Sample size reduction is a critical first step as many extractions require particle sizes in the micron range.</p>
                                <p class="bold">Omni International offers a broad range of suitable products for high throughput extraction of cannabis products.</p>
                                <p>The Bead Ruptor 24 supports rapid grinding of cannabis flowers and edibles for downstream extraction of cannabinoids, terpenes, flavonoids and allows samples homogenization for bioburden assays.With optional attachments, the Bead Ruptor 24 can grind cannabis samples in 2 ml to 50 mL tubes.</p>
